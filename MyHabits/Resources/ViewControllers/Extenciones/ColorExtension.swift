import UIKit
import Foundation

extension UIColor {
    
    static func myColor(dark: UIColor, any: UIColor) -> UIColor {
        UIColor { traitCollection in
            if traitCollection.userInterfaceStyle == .dark {
                return dark
            }
            else {
                return any
            }
        }
    }
    
    static var lightGrayColor: UIColor {
